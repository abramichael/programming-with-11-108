public class Node {
	
	private int info;
	private Node left, right;
	
	public int getInfo() {
		return info;
	}

	public Node getLeft() {
		return left;
	}
	
	public Node getRight() {
		return right;
	}
	
	public void setInfo(int info) {
		this.info = info;
	}
	
	public void setLeft(Node left) {
		this.left = left;
	}

	public void setRight(Node right) {
		this.right = right;
	}
	
}





